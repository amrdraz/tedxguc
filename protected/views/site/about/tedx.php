<p>
TEDx was created in the spirit of TED's mission, "ideas worth 
spreading." The program is designed to give communities, 
organizations and individuals the opportunity to stimulate dialogue 
through TED-like experiences at the local level.
</p>
<br/>
<p>
At TEDx events, a screening of TEDTalks videos -- or a combination 
of live presenters and TEDTalks videos -- sparks deep conversation 
and connections. TEDx events are fully planned and coordinated 
independently, on a community-by-community basis.
</p>
<br/>
<p>
<span>What you'll find at every TEDx event</span> 
<br/>
The content and design of each TEDx event is unique and developed 
independently, but all TEDx events have several features in 
common.
</p>
<br/>
<p>
<span>TED's celebrated format</span> 
<br/>
A suite of short, carefully prepared talks, 
demonstrations and performances (live, or just TEDTalks videos 
from TED.com) on a wide range of subjects to foster learning, inspiration and wonder -- and to provoke conversations that matter.
</p>
<br/>
<p>
<span>TEDTalks videos</span>
<br/>
A minimum of two pre-recorded talks from the acclaimed TEDTalks video series (these talks are available free on TED.com)
</p>
<br/>
<p>
<span>Bias-free programming</span> 
<br/>
Lack of any commercial, religious or political agenda
</p>