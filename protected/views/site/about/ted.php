<p>
We believe passionately in the power of ideas to change attitudes, 
lives and ultimately, the world. So we're building here a 
clearinghouse that offers free knowledge and inspiration from the 
world's most inspired thinkers, and also a community of curious 
souls to engage with ideas and each other. The TED site, launched 
April 2007, is an ever-evolving work in progress, and you're an 
important part of it. Have an idea? We want to hear from you.
</p>
<br/>
<p>
TED is a nonprofit organization devoted to Ideas Worth Spreading. Started as a four-day conference in California 26 years ago, TED has grown to support those world-changing ideas with multiple initiatives. At TED, the world's leading thinkers and doers are asked to give the talk of their lives in 18 minutes. Talks are then made available, free, at TED.com. TED speakers have included Bill Gates, Jane Goodall, Elizabeth Gilbert, Sir Richard Branson, Benoit Mandelbrot, Philippe Starck, Ngozi Okonjo-Iweala, Isabel Allende and former UK Prime Minister Gordon Brown. Two major TED events are held each year: The TED Conference takes place every spring in Long Beach, California (along with a parallel conference, TEDActive, in Palm Springs), and TEDGlobal is held each summer in Edinburgh, Scotland.
</p>
